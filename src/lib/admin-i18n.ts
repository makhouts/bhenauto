export type AdminDictionary = (typeof adminDictionaries)[AdminLocale];

export function getAdminDictionary(locale: AdminLocale): AdminDictionary {
    return adminDictionaries[locale];
}

export function tpl(template: string, values: Record<string, string | number>): string {
    return Object.entries(values).reduce((acc, [key, value]) => acc.replaceAll(`{${key}}`, String(value)), template);
}

export function toPublicLocale(locale: AdminLocale): PublicLocale {
    return locale;
}
