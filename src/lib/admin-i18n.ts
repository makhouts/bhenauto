import { fr as frDateFns, nl as nlDateFns } from "date-fns/locale";
import type { Locale as PublicLocale } from "@/lib/i18n";

export const ADMIN_LOCALE_COOKIE = "admin_locale";
export const adminLocales = ["nl", "fr"] as const;
export type AdminLocale = (typeof adminLocales)[number];

export function isValidAdminLocale(value: string | undefined | null): value is AdminLocale {
    return value === "nl" || value === "fr";
}

export function getAdminDateFnsLocale(locale: AdminLocale) {
    return locale === "fr" ? frDateFns : nlDateFns;
}

